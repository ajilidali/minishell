#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*cpy;
	int		i;

	i = 0;
	len = ft_strlen(s1);
	cpy = (char *)ft_calloc((len + 1), sizeof(char));
	if (cpy == NULL)
		return (NULL);
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
